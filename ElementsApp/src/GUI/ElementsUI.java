    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        
        try {
            Node e = elements.search(txtAcronym.getText());
            if (e != null) {
                txtDetails.setText(e.getAcronym() + "\n" + e.getMeaning()+"\n\n");
            } else {
                txtDetails.setText("No such Element");
            }
        } catch(Exception e) {
            txtDetails.setText("Something went wrong.. Search Again");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:                                       
    try {
        String keyToDelete = txtAcronym.getText().trim(); 
        if (keyToDelete.isEmpty()) {
            txtDetails.setText("Please enter an acronym to delete.");
            return; 
        }

        boolean deleted = elements.delete(keyToDelete); 

        if (deleted) {
            txtDetails.setText("Acronym '" + keyToDelete + "' deleted successfully."); 
        } else {
            txtDetails.setText("Acronym '" + keyToDelete + "' not found. Nothing deleted.");
        }
        txtAcronym.setText(""); 

    } catch (Exception ex) { 
        txtDetails.setText("Something went wrong during deletion: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

