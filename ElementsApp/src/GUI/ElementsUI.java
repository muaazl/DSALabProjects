    private void readData() {
try {
            String filepath = "C:\\Users\\muaaz\\Documents\\NetBeansProjects\\ElementsApp\\src\\elements.txt";
            FileReader fr = new FileReader(filepath);
            BufferedReader br = new BufferedReader(fr);
            String details[];
            String line = br.readLine();

            while (line != null) {
                details = line.split(",");
                elements.insert( details[0].trim(),details[1].trim());

                line = br.readLine();
            }

            br.close();
        } catch (IOException e1) {
            System.out.println("Error in File reading!!!");
        } catch (Exception e2) {
            System.out.println("Error in processing!!!");
        }
    }
