package Code;

/**
 *
 * @author muaaz
 */
public class ShortHandKey {
    private String key;
    private String meaning;

    public ShortHandKey(String key, String meaning) {
        this.key = key;
        this.meaning = meaning;
    }

    public String getKey() {
        return key;
    }

    public String getMeaning() {
        return meaning;
    }
    
}
