    private void btnNextTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextTicketActionPerformed
        // TODO add your handling code here:
        if (seatList.heapSize == 0) {
            JOptionPane.showMessageDialog(this, "List is Empty");
        }

        Member nextMember = seatList.remove();
        if (nextMember != null) {
            String message = "Member ID: " + nextMember.getName() +
                                   "\nMember Name: " + nextMember.getNumber()+
                                   "\nSeatNumber: " + seatNumber++;
            JOptionPane.showMessageDialog(this, message);
        }
    }//GEN-LAST:event_btnNextTicketActionPerformed

