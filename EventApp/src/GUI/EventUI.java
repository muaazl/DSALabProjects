    private void btnAddMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddMemberActionPerformed
        // TODO add your handling code here:
        String name = txtMemberName.getText();
        int id = Integer.parseInt(txtMemberID.getText());
        if (seatList.heapSize == seatList.maxSize) {
            JOptionPane.showMessageDialog(this, "Sorry, the list is full. Cannot add more members.", "List Full", JOptionPane.WARNING_MESSAGE);
        } else if (seatList.containsID(id)) {
            JOptionPane.showMessageDialog(this, "Member ID " + id + " already exists. Please use a unique ID.");
        } else {
            try {
            if (count <= 200) {
                Member m = new Member(name, id);
                seatList.insert(m);
                count++;
                JOptionPane.showMessageDialog(this, "Member Added");
            } else {
                JOptionPane.showMessageDialog(this, "Sorry List is full");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter correct member ID");
        }
        txtMemberName.setText("");
        txtMemberID.setText("");
        }
        
        
    }//GEN-LAST:event_btnAddMemberActionPerformed

